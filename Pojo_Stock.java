package PojoClass;

public class Pojo_Stock {

    private String juice_name;
    private int qty;
    private double rate;
    private int id;

    public String getJuice_name() {
        return juice_name;
    }

    public void setJuice_name(String juice_name) {
        this.juice_name = juice_name;
    }

    public int getQty() {
        return qty;
    }

    public void setQty(int qty) {
        this.qty = qty;
    }

    public double getRate() {
        return rate;
    }

    public void setRate(double rate) {
        this.rate = rate;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
    
}
